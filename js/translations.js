// Helper function to set text content based on translation keys in data-translate attributes
function applyTranslations(lang) {
  const dict = translations[lang] || translations.en;
  // Update current language label
  const currentLangEl = document.getElementById("currentLang");
  if (currentLangEl) currentLangEl.textContent = dict.currentLangLabel;

  // For each element having data-translate, replace text content
  document.querySelectorAll('[data-translate]').forEach((el) => {
    const keyPath = el.getAttribute('data-translate');
    const parts = keyPath.split('.');
    let value = dict;
    for (const p of parts) {
      value = value?.[p];
    }
    if (typeof value === 'string') {
      el.textContent = value;
    }
  });
}

window.WorkerHelperI18n = { translations, applyTranslations };
